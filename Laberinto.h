// PERSONAJE

struct Personaji {
	int X = inX;
	int Y = inY;
	int altura = 4;
	int ancho = 4;
	int vidas = 3;

	string Person[4] = {
		" O ",
		"[|]",
		"d|b",
		"L|L",
	};
}Personaje;


//BORRAR PERSONAJE
void borrarPersonaji() {
	for (int i = 0; i < Personaje.altura; i++) {

		Console::SetCursorPosition(Personaje.X, Personaje.Y + i);
		for (int j = 0; j < Personaje.ancho; j++) {
			cout << " ";
		}
		cout << endl;
	}
}

//DIBUJAR PERSONAJE
void dibujarPersonaji() {
	for (int i = 0; i < Personaje.altura; i++) {
		Console::SetCursorPosition(Personaje.X, Personaje.Y + i);

		cout << Personaje.Person[i];
	}
}


