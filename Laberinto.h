//PARA EL INT MAIN, COMPROBACION DE BLOQUES.

void Laberinto() {
	drawLaberinto(); //DIBUJAMOS EL LABERINTO

	bool jugar = true;
	while (jugar == true) {
		dibujarPersonaji(); //DIBUJAMOS EL PERSONAJE

		if (_kbhit()) {
			char tecla = toupper(_getch());

			int EQUIS = Personaje.X;
			int EYE = Personaje.Y;

			switch (tecla) {
			case 'W': EYE--;
				break;
			case 'S': EYE++;
				break;
			case 'A': EQUIS--;
				break;
			case 'D': EQUIS++;
				break;
			}

			bool autorizacion = true;
			bool nivel2punto2 = false;
			bool esquivoesquivo = false;

			for (int x = 0; x < Personaje.ancho; x++) {
				for (int y = 0; y < Personaje.altura; y++) {
					int punto = LaberintoMatriz[EYE + y][EQUIS + x];

					if (punto == 1) autorizacion = false;
					if (punto == 2) nivel2punto2 = true;
					if (punto == 3) esquivoesquivo = true;
					}
						
				}
			if (autorizacion) {
				borrarPersonaji();
				Personaje.X = EQUIS;
				Personaje.Y = EYE;
			}
			if (nivel2punto2) {
				Nivel2punto2();
			}
			if (esquivoesquivo) {
				Personaje.vidas--;
				borrarPersonaji();
				//RESETEAMOS AL PERSONAJE AL PRINCIPIO BROE
				Personaje.X = 2; Personaje.Y = 2;

				if (Personaje.vidas <= 0) {
					system("cls");
					cout << "MORISTE BRO" << endl;
					jugar = false;
				}
			}
		}

		Sleep(75);	//Recomendable usar la variable waitTime de Utils.h en su lugar.
	}
	

}